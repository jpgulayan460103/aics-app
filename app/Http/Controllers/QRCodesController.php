     public function decode(Request $request)
    {
        $request->validate([
            'qr_code_image' => 'required|image'
        ]);

        $file = $request->file('qr_code_image');
        $path = $file->getRealPath();

        $qrcode = new QrReader($path);
        $text = $qrcode->text(); 

        return response()->json(['decoded_text' => $text]);
    }
